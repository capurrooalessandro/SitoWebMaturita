            <div id="toggle4" class="text-justify mt-1">
              <p class="content">Il Fairlight CMI (Computer Musical Instrument) è stato il primo sintetizzatore-campionatore digitale.
                È stato progettato nel 1979 dai fondatori della Fairlight, Peter Vogel e Kim Ryrie ed era basato su un sintetizzatore a
                doppio microprocessore, il Qasar-M8, progettato da Tony Furse a Sydney.
                Ha acquistato popolarità nei primi anni Ottanta e ha finito per competere sul mercato con il Synclavier della New England Digital.
              </p>
