        <p class="text-justify introduction">
            Il 
            <b>sintetizzatore</b> 
            (abbreviato anche in 
            <b>synth</b> dal termine in inglese 
            <i>synthesizer</i>) è
            uno strumento musicale che appartiene alla famiglia degli elettrofoni. 
            È un apparato in grado di generare autonomamente segnali audio, sotto il controllo 
            di un musicista o di un sequencer. Si tratta di uno strumento che può generare imitazioni 
            di strumenti musicali reali o creare suoni ed effetti non esistenti in natura. Attualmente troviamo anche sintetizzatori virtuali
            (<b>VST</b>,
            <b>AU</b>,
            <b>RTAS</b>
            standard lanciato nel 1997), che assolvono a questo compito interamente a livello software e che si 
            appoggiano su schede sonore interne o esterne collegate ad un personal computer.
            <br>
            Il sintetizzatore è generalmente comandato per mezzo di una tastiera simile a quella del pianoforte, 
            comunque non mancano realizzazioni destinate ad essere gestite mediante il fiato, la pressione, 
            le corde di una chitarra o altri tipi di controller di nuova concezione come quelli a raggi infrarossi, a gesti e a onde cerebrali.
        </p>
        <h3 class="mt-3 pb-1 border-bottom border-secondary">Tipi di sintesi:</h3>
        <h4 class="ml-2 mt-1">Sintesi sottrattiva:</h4>
        <p class="text-justify ml-3 mb-2 content">
            Da un 
            <i>generatore di segnale</i> 
            con elevata produzione di armoniche (ad esempio onda quadra, onda triangolare, dente di sega, etc) 
            si interviene con un sistema di filtri allo scopo di modificare il timbro e quindi la forma d'onda. Esempi di sintesi sottrattiva 
            si possono ritrovare anche negli strumenti musicali tradizionali dove la selezione del timbro è ottenuta in maniera meccanica tramite 
            la cassa armonica come nella chitarra o nel violino. Inoltre è possibile variare la frequenza del filtro (cut off) e il fattore di merito Q 
            (Peak o resonance) tramite opportuni controlli. I filtri possono essere realizzati con tecnologia analogica (reti RC o componenti discreti)
            oppure con tecnologia digitale tramite 
            <b>DSP.</b> Altri parametri fondamentali nella catena di sintesi analogica sono l'inviluppo 
            (<b>ADSR</b>), il controllo
            del volume 
            (<b>VCA</b>) e gli effetti di vibrato 
            (<b>LFO</b>).
            <br>
            I primi sintetizzatori a sintesi sottrattiva erano implementati tramite sistemi modulari analogici 
            dove era possibile interconnettere e controllare ogni modulo a piacimento. In seguito sono stati sviluppati i primi sintetizzatori normalizzati dove
            l'utente poteva scegliere fra alcune configurazioni di base scelte dal costruttore.
            <br>
            Una grande rivoluzione è stata l'implementazione della sintesi sottrattiva nel dominio digitale, dove un semplice DSP può sostituire le funzioni di centinaia di moduli analogici.
        </p>
        <p class="mt-2 mb-2 text-justify content">
            Alcuni esempi di celebri sintetizzatori analogici a sintesi sottrattiva sono i sistemi modulari 
            <b>Moog</b>, i giapponesi 
            <b>Korg Monopoly</b> e 
            <b>Roland Jupiter-8</b>, e gli americani 
            <b>ARP 2600</b> e 
            <b>Sequential Circuits Prophet 5.</b>
            Tra i sintetizzatori digitali a sintesi sottrattiva vi sono il 
            <b>Clavia Nord Lead</b>, il 
            <b>Roland JP8000</b> e il 
            <b>Korg MS 2000</b>.
        </p>
        <h4 class="ml-2">Sintesi additiva:</h4>
        <p class="text-justify ml-3 mb-4 content">
            Partendo dal presupposto per il quale il timbro caratteristico di un dato strumento è prodotto dalla fondamentale più una determinata distribuzione delle armoniche,
            è possibile ricreare un suono naturale partendo dalla somma di un certo numero di frequenze fondamentali (segnali sinusoidali) e distribuendole nello spettro sonoro.
            Questa tecnica, pur permettendo teoricamente di riprodurre qualsiasi suono esistente, in realtà è di estrema complessità.
            <br>
            Infatti al contrario di quanto accade nella sintesi sottrattiva dove si agisce su un consistente numero di armoniche già patrimonio del segnale grezzo originale,
            nella sintesi additiva si ha la necessità di controllare
            separatamente un numero elevatissimo di componenti che molto probabilmente andranno modulate individualmente per ottenere una risposta convincente all'ascolto. 
            <br>
            Si tratta pertanto di una tecnica complessa che non ha incontrato molto successo nella produzione industriale di strumenti musicali elettronici, con la notevole eccezione 
            dei vari tipi di organi elettronici, però è interessante nell'ambito della ricerca.
        </p>
