        <h3 class="mt-2 mb-2 pb-1 border-bottom border-secondary">Cos'è un sintetizzatore:</h3>
        <figure class="mt-2 mr-sm-2 p-2 mb-0 float-md-left border border-dark" id="photos">
            <img class="img-fluid mx-auto d-flex border border-dark" src="img/1920px-Minimoog.jpg" alt="Esempio di Sintetizzatore" width="466.8">
